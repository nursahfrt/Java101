package PaketTemellerDegiskenler;

public class VeriturleriTamsayi {
    public static void main(String[] args) {
        System.out.println("Tamsayı Veri Tipleri");
 /*Tamsayı Veri Tipleri =
         Byte =       8 bit uzunluğunda , 127 & -128 aralığı
         Short =     16 bit uzunluğunda , 32767 & -32767 aralığı
         Integer =   32 bit uzunluğunda , 2147483647 & -2147483648 aralığı (en çok tercih edilen)
         Long =      64 bit uzunluğunda , 9223372036854775807 & -9223372036854775808 aralığı
Sadece boyut farkı var */
        byte  a = 127;
        short b = 32767;
        int   c = 2147483647;
        long  d = 922337203;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);





    }
}
