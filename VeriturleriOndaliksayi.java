package PaketTemellerDegiskenler;

public class VeriturleriOndaliksayi {
    public static void main(String[] args) {
        System.out.println("Ondalık Sayı Veri Tipleri");
 /*Ondalık Sayı Veri Tipleri =
         Float =       32 bit uzunluğunda. Bu ikisini ayırmak için floatta sayıdan sonra f ya da F harfi konulmalıdır. Basittir. hız ve bellek.
         Double =     64 bit uzunluğunda , Üst düzey matematiksel işlemlerde kullanılır.Yüksek duyarlı ve hassastır. Önerilendir.
*/
        float aa = 3.14F;
        float bb = 3.14f;
        float cc = 3;

        double dd = 3.14;
        double ee = 3;

        System.out.println(aa);
        System.out.println(bb);
        System.out.println(cc);
        System.out.println(dd);
        System.out.println(ee);
    }
}
