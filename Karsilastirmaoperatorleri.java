package PaketKosulluifadeler;

public class Karsilastirmaoperatorleri {
    public static void main(String[] args) {
        int x,y,z;

        x=5;
        y=10;
        z=15;

        System.out.println(x==y);   // eşit mi?
        System.out.println(x!=y);   // eşit değil mi?
        System.out.println(x>y);    // x y'den büyük mü?
        System.out.println(y>=z);   // y z'den büyük ya da eşit mi?
        System.out.println(x<y);    // x y'den küçük mü?
        System.out.println(y<=z);   // y z'den küçük ya da eşit mi?
    }
}
